package modelo;

/**
 * @author adalberto
 *
 */
public class ElementoHTML {

	/**
	 * Identificador Elemento HTML
	 */
	private int id;
	/**
	 * Nombre del Elemento HTML
	 */
	private String nombre;

	/**
	 * 
	 */
	public ElementoHTML() {
	}

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}

	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

}
